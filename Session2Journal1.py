float_num1 = 1.5
float_num2 = 2.5
int_num1 = 5
int_num2 = 10


sum_result = float_num1 + float_num2
print("The sum of two floating point numbers is ", sum_result)
print("The data type of the sum is ", type(sum_result))


difference_result = int_num1 - int_num2
print("\n The difference between two integers is ", difference_result)
print("The data type of the difference is", type(difference_result))

product_result = float_num1 * int_num2
print("\n The product of a floating point number and an integer", product_result)
print("The data type of the product is ", type(product_result))
