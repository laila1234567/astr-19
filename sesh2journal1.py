float_num1 = 7.8
float_num2 = 2.9
int_num1 = 15
int_num2 = 13

#Sum of two floating point numbers
sum_result = float_num1 + float_num2
print("Sum:", sum_result, "Data type:", type(sum_result))

#Difference between two integers
difference_result = int_num1 - int_num2
print("Difference:", difference_result, "Data type:", type(difference_result))

#Product of a floating point number and an integer
product_result = float_num1 * int_num1
print("Product:", product_result, "Data type:", type(product_result))
